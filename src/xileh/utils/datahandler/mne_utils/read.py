import mne
import re
from mne import create_info
from mne.io.constants import FIFF

from datetime import datetime


def read_epochs_fif(fname, info=None):
    f = mne.read_epochs(fname)
    try:
        ch_types = [re.findall('_([^_]*)_CH$', c['kind']._name)[0].lower()
                    for c in f.info['chs']]

    # TODO: check of the specific Exception and only escape this
    except Exception as e:
        print(f"Encountered exception {e} -- continue with legacy mode")
        ch_types = 'eeg'                # hack for legacy

    if info is None:
        # if none simply use from file
        info = create_info(f.info['ch_names'],
                           sfreq=f.info['sfreq'],
                           ch_types=ch_types
                           )
    else:
        info_obj = create_info(info.pop('ch_names'),
                               sfreq=info.pop('sfreq'),
                               ch_types=ch_types
                               )
        for k, v in info.items():
            info_obj[k] = v
        info = info_obj

    # change things that have been generated by the reading process
    for i, ch in enumerate(info['chs']):
        # unit_mul is named int after being read
        info['chs'][i]['unit_mul'] = int(ch['unit_mul'])

        # coord_frame has switched types
        # TODO: this one is not dynamic!!
        info['chs'][i]['coord_frame'] = FIFF.FIFFV_COORD_UNKNOWN

    # custom_ref_applied uses namedint instead of bool
    info['custom_ref_applied'] = bool(info['custom_ref_applied'])
    f.info = info

    return f


