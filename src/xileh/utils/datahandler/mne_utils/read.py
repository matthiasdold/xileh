def _construct_info_from_dict(ch_names, sfreq, chs, custom_ref_applied,
                              file_id, meas_id, meas_date, highpass, lowpass,
                              dig, ch_types='eeg', **kwargs):
    # this exists because the create_info function doesn't allow you to specify
    # a lot of the stuff
    # --> only casting to ch_types='eeg' will be a problem
    # --> derive the ch type from the 'chs' names
    try:
        ch_types = [re.findall('_([^_]*)_CH$', c['kind']._name)[0].lower()
                    for c in chs]

    # TODO: check of the specific Exception and only escape this
    except Exception as e:
        print(f"Encountered exception {e} -- continue with legacy mode")
        ch_types = 'eeg'                # hack for legacy

    info = create_info(ch_names, sfreq=sfreq, ch_types=ch_types)
    info['file_id'] = file_id
    info['meas_id'] = meas_id
    if isinstance(meas_date, tuple) or meas_date is None:
        info['meas_date'] = meas_date
    else:
        info['meas_date'] = datetime.strptime(meas_date, '%Y%m%d%H%M%S')

    info['highpass'] = highpass
    info['lowpass'] = lowpass
    info['dig'] = dig
    # change things that have been generated by the reading process

    for i, ch in enumerate(info['chs']):
        # unit_mul is named int after being read
        info['chs'][i]['unit_mul'] = int(ch['unit_mul'])

        # set scanno and logno
        info['chs'][i]['scanno'] = chs[i]['scanno']
        info['chs'][i]['logno'] = chs[i]['logno']
        info['chs'][i]['cal'] = chs[i]['cal']
        info['chs'][i]['loc'] = chs[i]['loc']

    # custom_ref_applied uses namedint instead of bool
    info['custom_ref_applied'] = bool(info['custom_ref_applied'])

    return info
